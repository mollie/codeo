/*
  We gaan een rekenmachine bouwen die kan vermenigvuldigen. Hiervoor heb je nodig:

  HTML:
  - Twee inputs, voor elk cijfer een
  - Een button, om de berekening uit te voeren
  - Een div om de uitkomst naartoe te schrijven, geef deze een naam

  JavaScript:
  - Een event listener om te luisteren naar de "Bereken" knop
  - Query selectors om de beide input waardes te selecteren
  - Query selector om de uitkomst div te selecteren en de som naartoe te schrijven
  - Een stukje code om de twee cijfers te vermenigvuldigen

  Bonus:
  - Maak je rekenmachine mooi met CSS
*/
let cijferLinks = document.querySelector('.cijferLinks');
let cijferRechts = document.querySelector('.cijferRechts');
let totaal = document.querySelector('span');

let knop = document.querySelector('button');

knop.addEventListener('click', berekenen);

function berekenen() {
  totaal.innerText = cijferLinks.value * cijferRechts.value;
}